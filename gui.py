class GUI:
    def __init__(self):
        self.erasable = erasableWrite(t, "", font=("Courier", 20, "bold"), align="center")

    def text(self, tx):
        t.goto(0, 0)
        temp = erasableWrite(t, tx, font=("Courier", 20, "bold"), align="center")
        self.erasable.clear()
        time.sleep(1)
        self.erasable = temp


