import turtle
import time


def erasableWrite(tortoise, name, font, align, reuse=None):
    eraser = turtle.Turtle() if reuse is None else reuse
    eraser.hideturtle()
    eraser.up()
    eraser.setposition(tortoise.position())
    eraser.write(name, font=font, align=align)
    return eraser


t = turtle.Turtle()
t.hideturtle()
t.up()


class GUI:
    def __init__(self):
        self.erasable = erasableWrite(t, "", font=("Courier", 20, "bold"), align="center")

    def text(self, tx):
        t.goto(0, 0)
        temp = erasableWrite(t, tx, font=("Courier", 20, "bold"), align="center")
        self.erasable.clear()
        time.sleep(1)
        self.erasable = temp


